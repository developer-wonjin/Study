SELECT EMPNO, ENAME
  FROM EMP
 WHERE SAL < 1000
    OR SAL > 3000;

SELECT EMPNO, ENAME
  FROM EMP
 WHERE SAL IN (1000, 3000);

SELECT EMPNO, ENAME
  FROM EMP
 WHERE SAL NOT IN (1000, 3000);

SELECT EMPNO, ENAME
  FROM EMP
 WHERE SAL > ANY (1000, 3000);

SELECT EMPNO, ENAME
  FROM EMP
 WHERE SAL NOT BETWEEN 1000 AND 3000;
