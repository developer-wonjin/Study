SELECT EMPNO, ENAME, SAL, ROWNUM AS RN
  FROM EMP
 WHERE DEPTNO IN (20, 30)
   AND ROWNUM <= 3
 ORDER BY SAL DESC;

SELECT EMPNO, ENAME, SAL, ROWNUM AS RN
  FROM EMP
 WHERE DEPTNO IN (20, 30)
