SELECT A.*, ROWNUM AS RN
  FROM (SELECT EMPNO, ENAME, SAL
          FROM EMP
         WHERE DEPTNO IN (20, 30)
         ORDER BY SAL DESC) A
 WHERE ROWNUM <= 3;
